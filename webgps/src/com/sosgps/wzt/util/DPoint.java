package com.sosgps.wzt.util;

public class DPoint {
	double x;
	double y;
	
	public DPoint(double x, double y){
		this.x = x;
		this.y = y;
	}
	
	public DPoint(){
	
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	
}
